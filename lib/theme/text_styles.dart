// Flutter imports:
import 'package:flutter/material.dart';

class AnilibriaTextStyle {
  static TextStyle bold(Color color) {
    return TextStyle(
      fontWeight: FontWeight.bold,
      fontSize: 15,
      color: color,
    );
  }

  static TextStyle small1(Color color) {
    return TextStyle(
      fontWeight: FontWeight.bold,
      fontSize: 10,
      color: color,
    );
  }

  static TextStyle small2(Color color) {
    return TextStyle(
      fontWeight: FontWeight.w400,
      fontSize: 10,
      color: color,
    );
  }

  static TextStyle title1(Color color) {
    return TextStyle(
      fontWeight: FontWeight.bold,
      fontSize: 12,
      color: color,
    );
  }

  static TextStyle body(Color color) {
    return TextStyle(
      fontWeight: FontWeight.w500,
      fontSize: 11,
      color: color,
    );
  }

  static TextStyle title2(Color color) {
    return TextStyle(
      fontWeight: FontWeight.w500,
      fontSize: 15,
      color: color,
    );
  }

  static TextStyle title3(Color color) {
    return TextStyle(
      fontWeight: FontWeight.w500,
      fontSize: 12,
      color: color,
    );
  }
}
